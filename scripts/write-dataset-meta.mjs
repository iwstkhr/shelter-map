import { existsSync, mkdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';

import { pathToFileURL } from 'node:url';

export const outputPath = 'app/generated/dataset-meta.ts';
const geojsonPath = 'public/assets/mergeFromCity_2.geojson';
const gzPath = 'public/assets/mergeFromCity_2.geojson.gz';

export function formatDatasetDate(date) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  return `${year}/${month}/${day}`;
}

export function readExistingUpdatedAt() {
  if (!existsSync(outputPath)) {
    return null;
  }

  const match = readFileSync(outputPath, 'utf8').match(/DATASET_UPDATED_AT = '([^']+)'/);
  return match?.[1] ?? null;
}

export function resolveUpdatedAt({ sourcePath } = {}) {
  if (process.env.DATASET_UPDATED_AT) {
    return process.env.DATASET_UPDATED_AT;
  }

  if (process.env.DATASET_LAST_MODIFIED) {
    return formatDatasetDate(new Date(process.env.DATASET_LAST_MODIFIED));
  }

  if (sourcePath && existsSync(sourcePath)) {
    return formatDatasetDate(statSync(sourcePath).mtime);
  }

  if (existsSync(geojsonPath)) {
    return formatDatasetDate(statSync(geojsonPath).mtime);
  }

  const existing = readExistingUpdatedAt();
  if (existing) {
    return existing;
  }

  if (existsSync(gzPath)) {
    return formatDatasetDate(statSync(gzPath).mtime);
  }

  throw new Error(`Missing dataset files and ${outputPath}`);
}

export function writeDatasetMeta(updatedAt) {
  mkdirSync('app/generated', { recursive: true });
  writeFileSync(
    outputPath,
    `// Generated by scripts/write-dataset-meta.mjs — do not edit.\nexport const DATASET_UPDATED_AT = '${updatedAt}'\n`,
  );
  console.log(`Wrote ${outputPath}: ${updatedAt}`);
}

const isMain =
  process.argv[1] !== undefined && import.meta.url === pathToFileURL(process.argv[1]).href;

if (isMain) {
  const updatedAt = resolveUpdatedAt();
  const existing = readExistingUpdatedAt();

  if (existing === updatedAt) {
    console.log(`Skipped ${outputPath}: already ${updatedAt}`);
  } else {
    writeDatasetMeta(updatedAt);
  }
}
